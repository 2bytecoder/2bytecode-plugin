    <script>
	$(document).ready(function() {
		function mainModal(mtitle, mdata, isError = false) {
			$("#mainModal h5#modalLabel").html(mtitle);
			// $("#mainModal div.modal-body").html(mdata);
			if (isError) {
				$("#mainModal div.modal-body").html('<i class="bi bi-exclamation-triangle-fill fs-1 d-block"></i>&nbsp;' + mdata);
			} else {
				$("#mainModal div.modal-body").html('<i class="bi bi-check-circle-fill fs-1 d-block"></i>&nbsp;' + mdata);
			}
			$("#mainModal").modal('show');
		}
		$("#mainModal").on('hidden.bs.modal', function(event) {
			$("#mainModal h5#modalLabel").html("");
			$("#mainModal div.modal-body").html("");
		});

		$(".contactus-form-submit").click(function(e) {
			e.preventDefault();
			var name = $('#contactus-form input[name="name"]').val();
            var email = $('#contactus-form input[name="email"]').val();
            var message = $('#contactus-form textarea[name="message"]').val();
			var nonce = $('#contactus-form input[name="_wpnonce"]').val();

			if (email == "" || nonce == "" || name == "" || message == "") {
				mainModal("Error", "Please Fill all the input fields.", true);
				return false;
			}

			$.ajax({
				type: 'POST',
				url: '<?php echo admin_url('admin-ajax.php'); ?>',
				data: {
					'name': name,
					'email': email,
                    'message' : message,
                    'nonce' : nonce,
					'action': 'tbc_contactus_response_content'
				},
				success: function(result) {

					if (result.success) {
						mainModal("Success", "<h4>SUCCESS!</h4><br><p>Your message has been sent! </p>", false);
					} else {
						mainModal("Error", "<h4>OH NO!</h4><br><p>We are unable to process your request right now.<br> Please Try after some time.</p>", true);
					}
				},
				error: function() {
					mainModal("Unable to send :(", "<h4>OH NO!</h4><br><p>We are unable to process your request right now.<br> Please Try after some time.</p>", true);
				}
			});

		});
	});
</script>